package Entidad;

import java.util.Date;

public class Persona_Ejercicio_12 {
    
    private String nombre;
    private Date fecha;

    public Persona_Ejercicio_12() {
    }

    public Persona_Ejercicio_12(String nombre, Date fecha) {
        this.nombre = nombre;
        this.fecha = fecha;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }
    
    
    
    
}
